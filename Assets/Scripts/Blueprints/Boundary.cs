struct Boundary
{
    public float Top, Bottom, Left, Right;  //declare float variables named Top, Bottom, Left and Right

    public Boundary(float top, float bottom, float left, float right)   //Boundary struct takes four parameters
    {
        Top = top;  //set value of Top to parameter top
        Bottom = bottom;    //set value of Bottom to parameter bottom
        Left = left;    //set value of Left to parameter left
        Right = right;  //set value Right to parameter right
    }
}